<h3 class="encabezado-qs">Innovación y tecnología para todos 🚀</h3>
<p>Bienvenido a <strong>RandomTech</strong>, tu tienda de ecommerce especializada en hardware. Desde nuestros inicios, hemos tenido un solo propósito: acercar la mejor tecnología a todos los apasionados del mundo digital.</p>

<h3 class="encabezado-qs">Nuestra trayectoria 📜</h3>
